# Area of Circle
radius = 10
area_circle = 3.14 * radius * radius
print("Area of circle:", round(area_circle, 2))

# Area of Triangle
base = 10
height = 5
area_triangle = 0.5 * base * height
print("Area of triangle:", round(area_triangle, 2))

# Area of Square
side = 4
area_square = side * side
print("Area of square:", area_square)
